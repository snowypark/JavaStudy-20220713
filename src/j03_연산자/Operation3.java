package j03_연산자;

public class Operation3 {

	public static void main(String[] args) {
		
		int result = 10;
		result = result + 10;
		System.out.println(result);
		
		result += 20;
		
		System.out.println(result);
		
		result *= 2;
		System.out.println(result);
		
		result %= 3;
		System.out.println(result);
		
	}

}
